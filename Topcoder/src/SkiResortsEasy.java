	public int minCost(int[] altitude)
	{
		int total = 0;
        int low = altitude[0];
        for(int i = 1; i < altitude.length; i++) {
            low = Math.min(low, altitude[i]);
            total += altitude[i] - low;
        }
        return total;
	}
	
